"""
   Python Program to Calculate the Area of a Triangle.

      # FORMULA : 1/2 * B *H
       HERE,
           B = BASE
           H = HEIGHT
"""


def area_of_triangle(b, h):
#    h is the height of the triangle
#    b is the base of triangle
    return 1/2 * b * h


print("area of the triangle:", area_of_triangle(5, 4))
